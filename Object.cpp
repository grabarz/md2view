//----------------------------------------------------------------------------------------------------

#include "Object.hpp"
//----------------------------------------------------------------------------------------------------

namespace MD2View
{
//----------------------------------------------------------------------------------------------------

Object::Object(ProgramVector& progs, ModelVector& mods, AnimationVector& anim)
	: model {mods}
	, program {progs}
	, animation {anim}
{
	animator.setAnimation(*animation);
}
//----------------------------------------------------------------------------------------------------

void Object::update(float dt)
{
	animator.update(dt);
}
//----------------------------------------------------------------------------------------------------

void Object::playPause()
{
	animator.playPause();
}
//----------------------------------------------------------------------------------------------------

void Object::nextAnimation()
{
	++animation;
	animator.setAnimation(*animation);
}
//----------------------------------------------------------------------------------------------------

void Object::prevAnimation()
{
	--animation;
	animator.setAnimation(*animation);
}
//----------------------------------------------------------------------------------------------------

void Object::nextModel()
{
	++model;
}
//----------------------------------------------------------------------------------------------------

void Object::nextProgram()
{
	++program;
}
//----------------------------------------------------------------------------------------------------

} // namespace MD2View
//----------------------------------------------------------------------------------------------------
