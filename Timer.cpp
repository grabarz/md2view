//----------------------------------------------------------------------------------------------------

#include "Timer.hpp"
//----------------------------------------------------------------------------------------------------

namespace MD2View
{
//----------------------------------------------------------------------------------------------------

Timer::Timer()
{
	startTime = SDL_GetTicks();
}
//----------------------------------------------------------------------------------------------------

void Timer::resume()
{
	if (paused)
	{
		currentTime = SDL_GetTicks();
		paused = false;
	}
}
//----------------------------------------------------------------------------------------------------

void Timer::pause()
{
	paused = true;
}
//----------------------------------------------------------------------------------------------------

void Timer::pauseResume()
{
	if (paused)
		resume();
	else
		pause();
}
//----------------------------------------------------------------------------------------------------

float Timer::getTime() const
{
	return currentTime / 1000.0;
}
//----------------------------------------------------------------------------------------------------

float Timer::getDt() const
{
	return currentTime / 1000.0;
}
//----------------------------------------------------------------------------------------------------

void Timer::update()
{
	if (!paused)
	{
		Uint32 now = SDL_GetTicks();

		currentDt = now - currentTime;

		currentTime = now;
	}
}
//----------------------------------------------------------------------------------------------------

} // namespace MD2View
